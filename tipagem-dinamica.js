let exemplo = 10; // exemplo é do tipo número (Number)
console.log(exemplo); 
console.log(typeof exemplo); 

exemplo = 'Olá, mundo!'; // agora exemplo é uma string (String)
console.log(exemplo); 
console.log(typeof exemplo); 

exemplo = true; // exemplo agora é um booleano (Boolean)
console.log(exemplo); 
console.log(typeof exemplo); 

